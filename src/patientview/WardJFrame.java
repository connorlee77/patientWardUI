    private void getPatients(Scanner s) {
        
        String[] lineData = s.nextLine().split(",");
        while(lineData[0] == null || lineData[0].equals("BedNum")) {
            lineData = s.nextLine().split(",");
        }
        
        while(s.hasNextLine()) {
            Patient patient = new Patient(lineData, "w001");
            lineData = s.nextLine().split(",");
            patients.add(patient);
        }
        
        Patient patient = new Patient(lineData, "w001");
        patients.add(patient);
    }

    
