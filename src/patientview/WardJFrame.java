    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged

        String p = (String) evt.getItem();
        switch (p) {
            case "Verbal":
                this.offsets[0] = 1;
                break;
            case "Pain":
                this.offsets[0] = 2;
                break;
            case "Unresponsive": 
                this.offsets[0] = 3;
                break;
            default:
                this.offsets[0] = 0;
                break;
        }
        
        if (timer.isRunning() == true) {
            timer.stop();
        } 
        WardActionListener page = new WardActionListener(patients, 
                this.jLabel_systemTime, this.psews, this.colors, this.offsets, this.actions);
        timer = new Timer(1000,  page);   

        if (timer.isRunning() == false) {
            timer.start();
        }  
    }//GEN-LAST:event_jComboBox1ItemStateChanged

